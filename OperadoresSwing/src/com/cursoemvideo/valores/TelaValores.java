    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        float n1,n2,divisao,raiz,potencia;
        int resto;
        
        n1 = Float.valueOf(txtNumerador.getText());
        n2 = Float.valueOf((txtDenominador.getText()));
        
        divisao = n1/n2;
        resto = (int) (n1%n2);
        raiz = (float) Math.sqrt(n1);
        potencia = (float) Math.pow(n1, n2);
        
        lblDivisao.setText(Float.toString(divisao));
        lblResto.setText(Integer.toString(resto));
        lblRaiz.setText(Float.toString(raiz));
        lblPow.setText(Float.toString(potencia));
        
    }//GEN-LAST:event_btnCalcularActionPerformed

